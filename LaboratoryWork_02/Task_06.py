import math

print("Введіть розмір бічної сторони a: ")
a = float(input())

print("Введіть розмір основи b: ")
b = float(input())

if a * 2 > b:
    c = 1 / 2 * b
    S = c * math.sqrt((a + c) * (a - c))
    if S % 2 == 0:
        S /= 2
    else:
        print("Не можу ділити на 2!")
    print("Площа рівнобедреного трикутника =", S)
else:
    print("Помилка!!! Сума довжин кожних двох сторін повинна бути більше довжини третьої сторони.")
