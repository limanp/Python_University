print("Введіть число a:")
a = int(input())
print("Введіть число b:")
b = int(input())

if a < b:
    sum = 0
    i = a
    while b >= i:
        sum += i
        i += 1
    print("Сума цілих чисел від", a, "до", b, "=", sum)
else:
    print("Помилка!!! Число [а] має бути меншим за [b]")